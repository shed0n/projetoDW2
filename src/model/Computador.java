package model;

public class Computador {
	
	private int codigo_comp;
	private String nome;
	private String placa_mae;
	private String processador;
	private String memoria;
	private String hd;
	private String video;
	private String outros;
	
	
	public int getCodigo_comp() {
		return codigo_comp;
	}
	
	public void setCodigo_comp(int codigo_comp) {
		this.codigo_comp = codigo_comp;
	}
	
	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public String getPlaca_mae() {
		return placa_mae;
	}
	
	public void setPlaca_mae(String placa_mae) {
		this.placa_mae = placa_mae;
	}
	
	public String getProcessador() {
		return processador;
	}
	
	public void setProcessador(String processador) {
		this.processador = processador;
	}
	
	public String getMemoria() {
		return memoria;
	}
	
	public void setMemoria(String memoria) {
		this.memoria = memoria;
	}
	
	public String getHd() {
		return hd;
	}
	
	public void setHd(String hd) {
		this.hd = hd;
	}
	
	public String getVideo() {
		return video;
	}
	
	public void setVideo(String video) {
		this.video = video;
	}
	
	public String getOutros() {
		return outros;
	}
	
	public void setOutros(String outros) {
		this.outros = outros;
	}
	
	

}
